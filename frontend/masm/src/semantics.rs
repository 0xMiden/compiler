use miden_assembly_syntax::ast::Instruction;

#[derive(Debug, Clone, Copy, PartialEq, Eq)]
pub(crate) enum InstructionSemantics {
    LiftAndInfer,
    InferOnly,
    Unsupported,
}

#[cfg(test)]
macro_rules! count_instruction_patterns {
    ($($pattern:pat),* $(,)?) => {
        <[()]>::len(&[$(count_instruction_patterns!(@unit $pattern)),*])
    };
    (@unit $pattern:pat) => {
        ()
    };
}

macro_rules! define_instruction_semantics {
    (
        lift_and_infer: [$($lift_and_infer:pat),* $(,)?],
        infer_only: [$($infer_only:pat),* $(,)?],
        unsupported: [$($unsupported:pat),* $(,)?],
    ) => {
        #[cfg(test)]
        pub(crate) const LIFT_AND_INFER_INSTRUCTION_VARIANT_COUNT: usize =
            count_instruction_patterns!($($lift_and_infer),*);
        #[cfg(test)]
        pub(crate) const INFER_ONLY_INSTRUCTION_VARIANT_COUNT: usize =
            count_instruction_patterns!($($infer_only),*);
        #[cfg(test)]
        pub(crate) const UNSUPPORTED_INSTRUCTION_VARIANT_COUNT: usize =
            count_instruction_patterns!($($unsupported),*);

        pub(crate) fn instruction_semantics(instruction: &Instruction) -> InstructionSemantics {
            match instruction {
                $($lift_and_infer => InstructionSemantics::LiftAndInfer,)*
                $($infer_only => InstructionSemantics::InferOnly,)*
                $($unsupported => InstructionSemantics::Unsupported,)*
            }
        }
    };
}

