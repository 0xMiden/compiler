/// Identifies a WIT procedure export in a dependency package.
#[derive(Clone, Debug, Eq, Hash, PartialEq)]
struct ProcedureRootKey {
    /// Fully-qualified WIT interface path, including package version.
    interface: String,
    /// WIT function name.
    function: String,
}

/// Four field elements that make up a foreign procedure root.
#[derive(Clone, Copy)]
struct ProcedureRoot {
    felts: [u64; 4],
}

impl ProcedureRootKey {
    /// Creates a root key for one WIT interface function.
    fn new(interface: impl Into<String>, function: impl Into<String>) -> Self {
        Self {
            interface: interface.into(),
            function: function.into(),
        }
    }
}

/// Returns the fully-qualified import path used for a resolved interface.
fn interface_import_path(resolve: &Resolve, interface_id: InterfaceId) -> Option<String> {
    let interface = &resolve.interfaces[interface_id];
    let interface_name = interface.name.as_deref()?;
    let package_id = interface.package?;
    Some(resolve.packages[package_id].name.interface_id(interface_name))
}

/// Resolves `felt` and `word` from `miden:base/core-types`.
fn resolve_core_types(resolve: &Resolve) -> syn::Result<CoreTypes> {
    let core_types = resolve
        .packages
        .iter()
        .find_map(|(_, package)| {
            if package.name.namespace != "miden" || package.name.name != "base" {
                return None;
            }

            package.interfaces.get("core-types").map(|interface_id| {
                let interface = &resolve.interfaces[*interface_id];
                (interface.types.get("felt").copied(), interface.types.get("word").copied())
            })
        })
        .ok_or_else(|| {
            Error::new(
                Span::call_site(),
                "failed to resolve miden:base/core-types package for FPI imports",
            )
        })?;

    let (Some(felt), Some(word)) = core_types else {
        return Err(Error::new(
            Span::call_site(),
            "miden:base/core-types is missing felt or word type definitions",
        ));
    };

    Ok(CoreTypes {
        felt: WitType::Id(felt),
        word: WitType::Id(word),
    })
}

/// Returns whether a dependency function receives a generated FPI companion.
fn is_fpi_source_function(function: &Function) -> bool {
    matches!(function.kind, FunctionKind::Freestanding)
        && !function.name.starts_with(WIT_FUNCTION_PREFIX)
}

/// Copies FPI variants of source functions into an empty private synthetic interface.
fn inject_functions_into_synthetic_interface(
    resolve: &mut Resolve,
    source_id: InterfaceId,
    synthetic_id: InterfaceId,
    core_types: CoreTypes,
) -> syn::Result<()> {
    let functions = resolve.interfaces[source_id]
        .functions
        .values()
        .filter(|function| is_fpi_source_function(function))
        .cloned()
        .collect::<Vec<_>>();

    let synthetic = &mut resolve.interfaces[synthetic_id];
    if !synthetic.types.is_empty() || !synthetic.functions.is_empty() {
        return Err(Error::new(
            Span::call_site(),
            "private FPI interface must be empty before generated functions are inserted",
        ));
    }

    let mut aliases = HashMap::new();
    let mut interface_types = Vec::new();
    let mut fpi_functions = Vec::with_capacity(functions.len());
    for function in functions {
        let fpi_name = format!("{WIT_FUNCTION_PREFIX}{}", function.name);
        let mut function = build_import_function(function, fpi_name.clone(), core_types);
        alias_function_types(
            resolve,
            synthetic_id,
            &mut function,
            &mut aliases,
            &mut interface_types,
        );
        fpi_functions.push((fpi_name, function));
    }

    let synthetic = &mut resolve.interfaces[synthetic_id];
    for (name, id) in interface_types {
        synthetic.types.insert(name, id);
    }
    for (name, function) in fpi_functions {
        synthetic.functions.insert(name, function);
    }

    Ok(())
}

/// Verifies that a canonical FPI interface structurally matches its source dependency.
///
/// This validates resolved WIT identity before Rust generation, including interfaces reused while
/// processing multiple imports from one synthetic package.
fn validate_synthetic_interface(
    resolve: &Resolve,
    source_id: InterfaceId,
    synthetic_id: InterfaceId,
    core_types: CoreTypes,
) -> syn::Result<()> {
    let source_functions = resolve.interfaces[source_id]
        .functions
        .values()
        .filter(|function| is_fpi_source_function(function))
        .collect::<Vec<_>>();
    let synthetic = &resolve.interfaces[synthetic_id];
    let invalid = || {
        Error::new(
            Span::call_site(),
            format!(
                "canonical private FPI interface `{}` is incompatible with source interface `{}`",
                interface_import_path(resolve, synthetic_id)
                    .unwrap_or_else(|| "<unknown>".to_string()),
                interface_import_path(resolve, source_id)
                    .unwrap_or_else(|| "<unknown>".to_string())
            ),
        )
    };

    if synthetic.functions.len() != source_functions.len() {
        return Err(invalid());
    }

    let expected_prefix = fpi_abi_params(core_types);
    let mut aliases = HashMap::new();
    for source in source_functions {
        let name = format!("{WIT_FUNCTION_PREFIX}{}", source.name);
        let Some(function) = synthetic.functions.get(&name) else {
            return Err(invalid());
        };
        if !matches!(function.kind, FunctionKind::Freestanding)
            || function.params.len() != source.params.len() + expected_prefix.len()
        {
            return Err(invalid());
        }
        for (param, (name, ty)) in function.params.iter().zip(expected_prefix) {
            if param.name != name
                || !synthetic_type_matches(resolve, synthetic_id, ty, param.ty, &mut aliases)
            {
                return Err(invalid());
            }
        }
        for (source, generated) in
            source.params.iter().zip(function.params.iter().skip(FPI_ABI_PARAM_COUNT))
        {
            if source.name != generated.name
                || !synthetic_type_matches(
                    resolve,
                    synthetic_id,
                    source.ty,
                    generated.ty,
                    &mut aliases,
                )
            {
                return Err(invalid());
            }
        }
        match (source.result, function.result) {
            (Some(source), Some(generated))
                if synthetic_type_matches(
                    resolve,
                    synthetic_id,
                    source,
                    generated,
                    &mut aliases,
                ) => {}
            (None, None) => {}
            _ => return Err(invalid()),
        }
    }

    let alias_ids = aliases
        .values()
        .copied()
        .filter(|id| resolve.types[*id].owner == TypeOwner::Interface(synthetic_id))
        .collect::<HashSet<_>>();
    if synthetic.types.len() != alias_ids.len()
        || synthetic.types.values().any(|id| !alias_ids.contains(id))
    {
        return Err(invalid());
    }

    Ok(())
}

/// Checks that a generated type is either unchanged or aliases its dependency-owned source type.
fn synthetic_type_matches(
    resolve: &Resolve,
    synthetic_id: InterfaceId,
    source: WitType,
    generated: WitType,
    aliases: &mut HashMap<TypeId, TypeId>,
) -> bool {
    let WitType::Id(source_id) = source else {
        return source == generated;
    };
    let WitType::Id(alias_id) = generated else {
        return false;
    };
    if let Some(expected) = aliases.get(&source_id) {
        return *expected == alias_id;
    }

    let source_type = &resolve.types[source_id];
    let generated_type = &resolve.types[alias_id];
    if source_type.name.is_none() && source_type.owner == TypeOwner::None {
        if generated_type.name.is_some() || generated_type.owner != TypeOwner::None {
            return false;
        }
        aliases.insert(source_id, alias_id);
        return synthetic_type_def_kind_matches(
            resolve,
            synthetic_id,
            &source_type.kind,
            &generated_type.kind,
            aliases,
        );
    }

    if generated_type.owner != TypeOwner::Interface(synthetic_id)
        || generated_type.kind != TypeDefKind::Type(WitType::Id(source_id))
    {
        return false;
    }

    aliases.insert(source_id, alias_id);
    true
}

/// Compares anonymous type constructors while following their nested synthetic aliases.
fn synthetic_type_def_kind_matches(
    resolve: &Resolve,
    synthetic_id: InterfaceId,
    source: &TypeDefKind,
    generated: &TypeDefKind,
    aliases: &mut HashMap<TypeId, TypeId>,
) -> bool {
    match (source, generated) {
        (TypeDefKind::Record(source), TypeDefKind::Record(generated)) => {
            source.fields.len() == generated.fields.len()
                && source.fields.iter().zip(&generated.fields).all(|(source, generated)| {
                    source.name == generated.name
                        && synthetic_type_matches(
                            resolve,
                            synthetic_id,
                            source.ty,
                            generated.ty,
                            aliases,
                        )
                })
        }
        (TypeDefKind::Resource, TypeDefKind::Resource) => true,
        (TypeDefKind::Handle(source), TypeDefKind::Handle(generated)) => {
            synthetic_handle_matches(resolve, synthetic_id, *source, *generated, aliases)
        }
        (TypeDefKind::Flags(source), TypeDefKind::Flags(generated)) => source == generated,
        (TypeDefKind::Tuple(source), TypeDefKind::Tuple(generated)) => {
            source.types.len() == generated.types.len()
                && source.types.iter().zip(&generated.types).all(|(source, generated)| {
                    synthetic_type_matches(resolve, synthetic_id, *source, *generated, aliases)
                })
        }
        (TypeDefKind::Variant(source), TypeDefKind::Variant(generated)) => {
            source.cases.len() == generated.cases.len()
                && source.cases.iter().zip(&generated.cases).all(|(source, generated)| {
                    source.name == generated.name
                        && synthetic_optional_type_matches(
                            resolve,
                            synthetic_id,
                            source.ty,
                            generated.ty,
                            aliases,
                        )
                })
        }
        (TypeDefKind::Enum(source), TypeDefKind::Enum(generated)) => source == generated,
        (TypeDefKind::Option(source), TypeDefKind::Option(generated))
        | (TypeDefKind::List(source), TypeDefKind::List(generated))
        | (TypeDefKind::Type(source), TypeDefKind::Type(generated)) => {
            synthetic_type_matches(resolve, synthetic_id, *source, *generated, aliases)
        }
        (TypeDefKind::Result(source), TypeDefKind::Result(generated)) => {
            synthetic_optional_type_matches(resolve, synthetic_id, source.ok, generated.ok, aliases)
                && synthetic_optional_type_matches(
                    resolve,
                    synthetic_id,
                    source.err,
                    generated.err,
                    aliases,
                )
        }
        (TypeDefKind::Map(source_key, source_value), TypeDefKind::Map(key, value)) => {
            synthetic_type_matches(resolve, synthetic_id, *source_key, *key, aliases)
                && synthetic_type_matches(resolve, synthetic_id, *source_value, *value, aliases)
        }
        (
            TypeDefKind::FixedLengthList(source, source_len),
            TypeDefKind::FixedLengthList(generated, generated_len),
        ) => {
            source_len == generated_len
                && synthetic_type_matches(resolve, synthetic_id, *source, *generated, aliases)
        }
        (TypeDefKind::Future(source), TypeDefKind::Future(generated))
        | (TypeDefKind::Stream(source), TypeDefKind::Stream(generated)) => {
            synthetic_optional_type_matches(resolve, synthetic_id, *source, *generated, aliases)
        }
        (TypeDefKind::Unknown, TypeDefKind::Unknown) => true,
        _ => false,
    }
}

/// Compares optional nested types in anonymous WIT constructors.
fn synthetic_optional_type_matches(
    resolve: &Resolve,
    synthetic_id: InterfaceId,
    source: Option<WitType>,
    generated: Option<WitType>,
    aliases: &mut HashMap<TypeId, TypeId>,
) -> bool {
    match (source, generated) {
        (Some(source), Some(generated)) => {
            synthetic_type_matches(resolve, synthetic_id, source, generated, aliases)
        }
        (None, None) => true,
        _ => false,
    }
}

/// Compares resource handles in anonymous WIT constructors.
fn synthetic_handle_matches(
    resolve: &Resolve,
    synthetic_id: InterfaceId,
    source: Handle,
    generated: Handle,
    aliases: &mut HashMap<TypeId, TypeId>,
) -> bool {
    match (source, generated) {
        (Handle::Own(source), Handle::Own(generated))
        | (Handle::Borrow(source), Handle::Borrow(generated)) => synthetic_type_matches(
            resolve,
            synthetic_id,
            WitType::Id(source),
            WitType::Id(generated),
            aliases,
        ),
        _ => false,
    }
}

/// Replaces cross-interface function types with local aliases to their original definitions.
fn alias_function_types(
    resolve: &mut Resolve,
    synthetic_id: InterfaceId,
    function: &mut Function,
    aliases: &mut HashMap<TypeId, TypeId>,
    interface_types: &mut Vec<(String, TypeId)>,
) {
    for param in &mut function.params {
        alias_wit_type(resolve, synthetic_id, &mut param.ty, aliases, interface_types);
    }
    if let Some(result) = &mut function.result {
        alias_wit_type(resolve, synthetic_id, result, aliases, interface_types);
    }
}

/// Replaces one resolved type ID with a valid synthetic representation.
fn alias_wit_type(
    resolve: &mut Resolve,
    synthetic_id: InterfaceId,
    ty: &mut WitType,
    aliases: &mut HashMap<TypeId, TypeId>,
    interface_types: &mut Vec<(String, TypeId)>,
) {
    let WitType::Id(source_id) = ty else {
        return;
    };
    if let Some(alias_id) = aliases.get(source_id) {
        *source_id = *alias_id;
        return;
    }

    let source_id_value = *source_id;
    let source_type = resolve.types[source_id_value].clone();
    if source_type.name.is_none() && source_type.owner == TypeOwner::None {
        let mut kind = source_type.kind;
        alias_type_def_kind(resolve, synthetic_id, &mut kind, aliases, interface_types);
        let clone_id = resolve.types.alloc(TypeDef {
            name: None,
            kind,
            owner: TypeOwner::None,
            docs: source_type.docs,
            stability: source_type.stability,
            span: source_type.span,
        });
        aliases.insert(source_id_value, clone_id);
        *source_id = clone_id;
        return;
    }

    let name = format!("miden-fpi-type-{}", interface_types.len());
    let alias_id = resolve.types.alloc(TypeDef {
        name: Some(name.clone()),
        kind: TypeDefKind::Type(WitType::Id(source_id_value)),
        owner: TypeOwner::Interface(synthetic_id),
        docs: Docs::default(),
        stability: Default::default(),
        span: WitSpan::default(),
    });
    aliases.insert(source_id_value, alias_id);
    interface_types.push((name, alias_id));
    *source_id = alias_id;
}

/// Rewrites every nested type reference in one cloned anonymous WIT constructor.
fn alias_type_def_kind(
    resolve: &mut Resolve,
    synthetic_id: InterfaceId,
    kind: &mut TypeDefKind,
    aliases: &mut HashMap<TypeId, TypeId>,
    interface_types: &mut Vec<(String, TypeId)>,
) {
    match kind {
        TypeDefKind::Record(record) => {
            for field in &mut record.fields {
                alias_wit_type(resolve, synthetic_id, &mut field.ty, aliases, interface_types);
            }
        }
        TypeDefKind::Handle(handle) => {
            alias_handle(resolve, synthetic_id, handle, aliases, interface_types);
        }
        TypeDefKind::Tuple(tuple) => {
            for ty in &mut tuple.types {
                alias_wit_type(resolve, synthetic_id, ty, aliases, interface_types);
            }
        }
        TypeDefKind::Variant(variant) => {
            for case in &mut variant.cases {
                if let Some(ty) = &mut case.ty {
                    alias_wit_type(resolve, synthetic_id, ty, aliases, interface_types);
                }
            }
        }
        TypeDefKind::Option(ty) | TypeDefKind::List(ty) | TypeDefKind::Type(ty) => {
            alias_wit_type(resolve, synthetic_id, ty, aliases, interface_types);
        }
        TypeDefKind::Result(result) => {
            for ty in [&mut result.ok, &mut result.err].into_iter().flatten() {
                alias_wit_type(resolve, synthetic_id, ty, aliases, interface_types);
            }
        }
        TypeDefKind::Map(key, value) => {
            alias_wit_type(resolve, synthetic_id, key, aliases, interface_types);
            alias_wit_type(resolve, synthetic_id, value, aliases, interface_types);
        }
        TypeDefKind::FixedLengthList(ty, _) => {
            alias_wit_type(resolve, synthetic_id, ty, aliases, interface_types);
        }
        TypeDefKind::Future(ty) | TypeDefKind::Stream(ty) => {
            if let Some(ty) = ty {
                alias_wit_type(resolve, synthetic_id, ty, aliases, interface_types);
            }
        }
        TypeDefKind::Resource
        | TypeDefKind::Flags(_)
        | TypeDefKind::Enum(_)
        | TypeDefKind::Unknown => {}
    }
}

/// Rewrites the resource type referenced by one cloned anonymous handle.
fn alias_handle(
    resolve: &mut Resolve,
    synthetic_id: InterfaceId,
    handle: &mut Handle,
    aliases: &mut HashMap<TypeId, TypeId>,
    interface_types: &mut Vec<(String, TypeId)>,
) {
    let id = match handle {
        Handle::Own(id) | Handle::Borrow(id) => id,
    };
    let mut ty = WitType::Id(*id);
    alias_wit_type(resolve, synthetic_id, &mut ty, aliases, interface_types);
    let WitType::Id(alias) = ty else {
        unreachable!("resource handles always reference a resolved type ID");
    };
    *id = alias;
}

/// Rejects dependency functions that use the namespace reserved for generated FPI imports.
fn validate_reserved_fpi_namespace<'a>(
    import: &str,
    functions: impl IntoIterator<Item = &'a Function>,
) -> syn::Result<()> {
    if let Some(function) = functions
        .into_iter()
        .find(|function| function.name.starts_with(WIT_FUNCTION_PREFIX))
    {
        return Err(Error::new(
            Span::call_site(),
            format!(
                "dependency interface `{import}` defines function `{}` with reserved FPI prefix \
                 `{}`; generated FPI imports use WIT names starting with `{}` and Rust names \
                 starting with `{}`, so dependency functions must use a different name",
                function.name, WIT_FUNCTION_PREFIX, WIT_FUNCTION_PREFIX, RUST_FUNCTION_PREFIX
            ),
        ));
    }

    Ok(())
}

/// Builds the caller-side WIT import that forwards through `execute_foreign_procedure`.
fn build_import_function(function: Function, fpi_name: String, core_types: CoreTypes) -> Function {
    let mut params = Vec::with_capacity(function.params.len() + FPI_ABI_PARAM_COUNT);
    params.extend(fpi_abi_params(core_types).map(|(name, ty)| Param {
        name: name.to_string(),
        ty,
        span: WitSpan::default(),
    }));
    params.extend(function.params);

    Function {
        name: fpi_name,
        kind: FunctionKind::Freestanding,
        params,
        result: function.result,
        docs: Docs::default(),
        // The generated function belongs to a private synthetic package, so source-package
        // feature metadata cannot be copied across package ownership boundaries.
        stability: Default::default(),
        span: WitSpan::default(),
    }
}

/// Returns the WIT parameters prepended by the FPI calling convention.
fn fpi_abi_params(core_types: CoreTypes) -> [(&'static str, WitType); FPI_ABI_PARAM_COUNT] {
    [
        (FPI_ABI_PARAM_NAMES[0], core_types.felt),
        (FPI_ABI_PARAM_NAMES[1], core_types.felt),
        (FPI_ABI_PARAM_NAMES[2], core_types.word),
    ]
}

/// Walks generated bindings and collects the import-module functions selected by `filter`.
pub(crate) fn collect_import_modules(
    items: &[Item],
    filter: &dyn Fn(&ItemFn) -> bool,
) -> syn::Result<Vec<Module>> {
    let mut modules = Vec::new();
    collect_modules(items, &mut Vec::new(), &mut modules, filter)?;
    Ok(modules)
}

/// Recursively walks all modules and collects selected functions from leaf import modules.
fn collect_modules(
    items: &[Item],
    path: &mut Vec<syn::Ident>,
    modules_out: &mut Vec<Module>,
    filter: &dyn Fn(&ItemFn) -> bool,
) -> syn::Result<()> {
    for item in items.iter() {
        if let Item::Mod(module) = item {
            path.push(module.ident.clone());
            if let Some((_, ref content)) = module.content {
                collect_modules(content, path, modules_out, filter)?;
                collect_functions_from_module(content, path, modules_out, filter);
            }
            path.pop();
        }
    }

    Ok(())
}

/// Collects selected generated free functions from a leaf import module.
fn collect_functions_from_module(
    items: &[Item],
    path: &[syn::Ident],
    modules_out: &mut Vec<Module>,
    filter: &dyn Fn(&ItemFn) -> bool,
) {
    if !should_generate_struct(path, items) {
        return;
    }

    let functions = items
        .iter()
        .filter_map(|item| match item {
            Item::Fn(func) if filter(func) => Some(func.clone()),
            _ => None,
        })
        .collect::<Vec<_>>();

    if functions.is_empty() {
        return;
    }

    modules_out.push(Module {
        module_path: path.to_vec(),
        path_string: format_module_path(path),
        functions,
    });
}

/// Builds the per-component account API traits for the user-provided account wrapper struct.
///
/// Each selected component (`package::Interface` reference) expands to one trait — named after the
/// interface (or its `as Alias`), with the wrapper's visibility — whose default methods dispatch
/// between the native (active) account and a foreign account, plus an empty `impl … for <Wrapper>`
/// that attaches it. Emitting one trait per component — rather than inherent methods on the wrapper
/// — lets two components that export the same method name coexist on one wrapper, disambiguated by
/// the caller with `<Wrapper as Interface>::method(account, ..)`.
///
/// `refs` is the reference list `dependencies` was selected from (same order); each reference is
/// the single source of its component's generated trait name.
pub(crate) fn augment_foreign_account_bindings(
    bindings: TokenStream2,
    account_struct: ItemStruct,
    dependencies: Vec<SelectedDependency>,
    refs: &[DependencyRef],
    fpi_imports: &[FpiImportSpec],
    binding_module_ident: syn::Ident,
) -> syn::Result<TokenStream2> {
    let file: File = syn::parse2(bindings)?;
    let native_modules = collect_import_modules(&file.items, &is_plain_import_function)?;
    let foreign_modules = collect_import_modules(&file.items, &is_fpi_import_function)?;

    // Each selected reference is checked against both module sets in the per-reference loop below,
    // so a missing module surfaces there as a specific "no callable exports" error per interface.
    // `dependencies` was selected from `refs` in order, so zip them here rather than threading a
    // separate parallel vector — the reference is the one source of the generated trait name.
    let dependencies = dependencies
        .into_iter()
        .zip(refs)
        .map(|(dependency, reference)| load_dependency(dependency, reference.trait_ident().clone()))
        .collect::<syn::Result<Vec<_>>>()?;
    let struct_item = foreign_account_struct(&account_struct)?;
    let constructor_item = constructor_impl(&account_struct);
    let account_binding_item = account_binding_impl(&account_struct);
    let active_account_item = active_account_impl(&account_struct);

    let mut trait_items = Vec::with_capacity(dependencies.len());
    let mut include_paths = Vec::new();

    // Iterate the selected references (not the generated modules) so a reference whose interface
    // has no callable exports is reported per-reference instead of being silently skipped — a
    // reference maps 1:1 to a generated trait, so every one must produce a module.
    for dependency in &dependencies {
        let Some(native_module) = native_modules
            .iter()
            .find(|module| module.path_string == dependency.module_path)
        else {
            return Err(Error::new(
                dependency.trait_ident.span(),
                format!(
                    "account interface `{}` has no callable exports; a referenced interface must \
                     export at least one function",
                    dependency.import
                ),
            ));
        };
        let import = fpi_imports
            .iter()
            .find(|import| import.source_import() == dependency.import)
            .ok_or_else(|| {
                Error::new(
                    dependency.trait_ident.span(),
                    format!(
                        "failed to find private FPI import metadata for `{}`",
                        dependency.import
                    ),
                )
            })?;
        let synthetic_module_path = import.synthetic_module_path();
        let foreign_module = foreign_modules
            .iter()
            .find(|module| module.path_string == synthetic_module_path)
            .ok_or_else(|| {
                Error::new(
                    dependency.trait_ident.span(),
                    format!(
                        "failed to find generated private FPI bindings for `{}`",
                        dependency.import
                    ),
                )
            })?;

        if !include_paths.iter().any(|path| path == &dependency.package_path) {
            include_paths.push(dependency.package_path.clone());
        }

        let mut signature_module_path = Vec::with_capacity(native_module.module_path.len() + 1);
        signature_module_path.push(binding_module_ident.clone());
        signature_module_path.extend(native_module.module_path.iter().cloned());

        let mut methods = Vec::with_capacity(foreign_module.functions.len());
        for foreign_func in &foreign_module.functions {
            let native_ident = method_ident(foreign_func)?;
            let native_func = native_module
                .functions
                .iter()
                .find(|func| func.sig.ident == native_ident)
                .ok_or_else(|| {
                    Error::new(
                        foreign_func.sig.ident.span(),
                        format!(
                            "generated private FPI function `{}` has no matching native function",
                            foreign_func.sig.ident
                        ),
                    )
                })?;
            let wit_name = function_wit_name(foreign_func)?;
            let root_key = ProcedureRootKey::new(dependency.import.as_str(), wit_name.as_str());
            let root = dependency.roots.get(&root_key).ok_or_else(|| {
                Error::new(
                    foreign_func.sig.ident.span(),
                    format!(
                        "failed to find procedure root for `{}#{wit_name}` in package '{}'",
                        dependency.import,
                        dependency.package_path.display()
                    ),
                )
            })?;
            methods.push(build_wrapper_method(
                native_func,
                foreign_func,
                &signature_module_path,
                quote!(#binding_module_ident),
                &native_module.module_path,
                &foreign_module.module_path,
                *root,
            )?);
        }

        if methods.len() != native_module.functions.len() {
            return Err(Error::new(
                dependency.trait_ident.span(),
                format!(
                    "generated private FPI bindings for `{}` do not match its native functions",
                    dependency.import
                ),
            ));
        }

        trait_items.push(build_account_trait(
            &account_struct.vis,
            &dependency.trait_ident,
            &account_struct.ident,
            dependency.import.as_str(),
            methods,
        ));
    }

    let bindings = file.into_token_stream();
    let package_includes = include_paths
        .into_iter()
        .map(|path| {
            let utf8_path = path.to_str().ok_or_else(|| {
                Error::new(
                    Span::call_site(),
                    format!("path '{}' contains invalid UTF-8", path.display()),
                )
            })?;
            Ok(quote! {
                const _: &[u8] = include_bytes!(#utf8_path);
            })
        })
        .collect::<syn::Result<Vec<_>>>()?;

    Ok(quote! {
        #[doc(hidden)]
        #[allow(dead_code)]
        pub mod #binding_module_ident {
            #bindings
        }

        #struct_item
        #constructor_item
        #account_binding_item
        #(#trait_items)*
        #active_account_item
        #(#package_includes)*
    })
}

/// Builds one generated component trait and its empty attachment impl for the account wrapper.
///
/// The trait is emitted with the wrapper struct's visibility (`vis`): a `pub` wrapper gets a `pub`
/// component trait so it can be imported cross-module, while a private wrapper keeps its component
/// trait private rather than expanding the user's public API.
fn build_account_trait(
    vis: &syn::Visibility,
    trait_ident: &syn::Ident,
    account_ident: &syn::Ident,
    import: &str,
    methods: Vec<TraitItemFn>,
) -> TokenStream2 {
    let trait_doc = format!(
        "Generated account API for the `{import}` component.\n\nEach method invokes the active \
         account by default, or the foreign account a `{account_ident}` is bound to (via \
         `{account_ident}::new`) through `execute_foreign_procedure`. When several component \
         traits on `{account_ident}` share a method name, disambiguate the call with \
         `<{account_ident} as {trait_ident}>::method(account, ..)`."
    );
    quote! {
        #[doc = #trait_doc]
        // The generated trait name is the interface segment (or `as Alias`) as written; allow a
        // non-UpperCamelCase name rather than fire `non_camel_case_types` on code the user cannot
        // annotate.
        #[allow(non_camel_case_types)]
        #vis trait #trait_ident: ::miden::active_account::AccountBinding {
            #(#methods)*
        }

        // Empty attachment impl: the default method bodies above are the implementation, reached
        // through the `AccountBinding` supertrait that `#account_ident` implements.
        impl #trait_ident for #account_ident {}
    }
}

/// Builds one component-trait default method that dispatches between a native and an FPI call.
///
/// The wrapper struct can stand in for either the transaction's native (active) account or a
/// foreign account. The generated method reads the binding target through the `AccountBinding`
/// supertrait at runtime: when set it forwards through the generated `fpi_*` import
/// (`execute_foreign_procedure`); otherwise it calls the dependency's native import directly.
fn build_wrapper_method(
    native_func: &ItemFn,
    foreign_func: &ItemFn,
    signature_module_path: &[syn::Ident],
    call_base_path: TokenStream2,
    native_call_module_path: &[syn::Ident],
    foreign_call_module_path: &[syn::Ident],
    procedure_root: ProcedureRoot,
) -> syn::Result<TraitItemFn> {
    validate_fpi_signature(native_func, foreign_func)?;

    let foreign_fn_ident = foreign_func.sig.ident.clone();
    let native_fn_ident = native_func.sig.ident.clone();
    let mut sig = native_func.sig.clone();
    sig.inputs.clear();
    sig.inputs.push(parse_quote!(&self));
    sig.inputs.extend(native_func.sig.inputs.iter().cloned());
    qualify_signature_types(&mut sig, signature_module_path);

    let arg_idents = collect_arg_idents(native_func)?;
    let root_tokens = procedure_root_tokens(procedure_root);

    let native_path = append_module_path(call_base_path.clone(), native_call_module_path);
    let foreign_path = append_module_path(call_base_path, foreign_call_module_path);

    let foreign_call = quote! {
        #foreign_path :: #foreign_fn_ident(
            __miden_foreign_account_id.prefix,
            __miden_foreign_account_id.suffix,
            #root_tokens,
            #(#arg_idents),*
        )
    };
    let native_call = quote! {
        #native_path :: #native_fn_ident(#(#arg_idents),*)
    };
    // Read the binding target through `AccountBinding` (the trait's supertrait) rather than the
    // wrapper's private field, because a trait default method body cannot name the concrete
    // wrapper's private field. The call is fully qualified so a component method that also happens
    // to be named `foreign_account_id` does not interfere with the dispatch.
    let dispatch = quote! {
        match ::miden::active_account::AccountBinding::foreign_account_id(self) {
            ::core::option::Option::Some(__miden_foreign_account_id) => { #foreign_call }
            ::core::option::Option::None => { #native_call }
        }
    };

    let method_doc = format!(
        "Invokes `{}` on the native account, or through `execute_foreign_procedure` when this \
         binding targets a foreign account.",
        native_fn_ident.to_string().to_kebab_case()
    );

    let body_tokens = match &sig.output {
        ReturnType::Default => quote!({ #dispatch; }),
        _ => quote!({ #dispatch }),
    };

    syn::parse2(quote! {
        #[doc = #method_doc]
        #[inline(always)]
        #sig #body_tokens
    })
}

/// Appends a generated Rust module path to a tokenized base path.
fn append_module_path(mut base: TokenStream2, module_path: &[syn::Ident]) -> TokenStream2 {
    for ident in module_path {
        base = quote! { #base :: #ident };
    }
    base
}

/// Verifies that a private FPI binding has the native function name and FPI ABI prefix.
///
/// Resolved WIT types are compared by [`validate_synthetic_interface`]. Rust aliases may render
/// with different token spellings, so this layer checks only properties introduced by wit-bindgen
/// and leaves alias compatibility to Rust's type checker.
pub(crate) fn validate_fpi_signature(native: &ItemFn, foreign: &ItemFn) -> syn::Result<()> {
    if method_ident(foreign)? != native.sig.ident {
        return Err(Error::new(
            foreign.sig.ident.span(),
            format!(
                "generated private FPI function `{}` does not match native function `{}`",
                foreign.sig.ident, native.sig.ident
            ),
        ));
    }

    let foreign_inputs = foreign.sig.inputs.iter().collect::<Vec<_>>();
    let native_inputs = native.sig.inputs.iter().collect::<Vec<_>>();
    if foreign_inputs.len() != native_inputs.len() + FPI_ABI_PARAM_COUNT {
        return Err(Error::new(
            foreign.sig.ident.span(),
            "generated FPI function is missing account id and procedure root parameters",
        ));
    }

    // These paths are the Rust representations installed for `felt` and `word` by
    // `generate::push_default_with_entries`.
    let expected_prefix: [syn::Type; FPI_ABI_PARAM_COUNT] = [
        parse_quote!(::miden::Felt),
        parse_quote!(::miden::Felt),
        parse_quote!(::miden::Word),
    ];
    for (input, expected) in foreign_inputs.iter().take(FPI_ABI_PARAM_COUNT).zip(&expected_prefix) {
        let syn::FnArg::Typed(input) = input else {
            return Err(Error::new_spanned(
                input,
                "generated FPI ABI prefix cannot contain a receiver",
            ));
        };
        if !tokens_equal(&input.ty, expected) {
            return Err(Error::new_spanned(
                &input.ty,
                "generated FPI ABI prefix must be `felt, felt, word`",
            ));
        }
    }

    Ok(())
}

/// Compares syntax nodes after normalizing their token formatting.
fn tokens_equal(left: &impl ToTokens, right: &impl ToTokens) -> bool {
    left.to_token_stream().to_string() == right.to_token_stream().to_string()
}

/// Builds the `AccountBinding` impl that exposes the wrapper's foreign-account id to the component
/// traits, so their default methods can dispatch without touching the private field.
fn account_binding_impl(account_struct: &ItemStruct) -> TokenStream2 {
    let ident = &account_struct.ident;
    quote! {
        impl ::miden::active_account::AccountBinding for #ident {
            #[inline(always)]
            fn foreign_account_id(&self) -> ::core::option::Option<::miden::AccountId> {
                self.foreign_account_id
            }
        }
    }
}

/// Replaces an empty marker struct with one that optionally stores a foreign account id.
///
/// A `None` id selects the transaction's native (active) account; a `Some` id selects a foreign
/// account reached through FPI.
fn foreign_account_struct(account_struct: &ItemStruct) -> syn::Result<ItemStruct> {
    let attrs = &account_struct.attrs;
    let vis = &account_struct.vis;
    let ident = &account_struct.ident;
    // `Default` is the only trait the macro derives: the note/tx-script entrypoint account is
    // built through `AccountWrapper::active()` (= `Self::default()`), whose `foreign_account_id`
    // is `None` (the active account). Conveniences such as `Clone`/`Copy`/`Debug` are left
    // to the user. The derive is skipped when the user already requests `Default`, because their
    // attributes are re-emitted verbatim and a second `Default` impl would conflict.
    let derive_default =
        (!user_derived_names(attrs).contains("Default")).then(|| quote!(#[derive(Default)]));
    syn::parse2(quote! {
        #(#attrs)*
        #derive_default
        #vis struct #ident {
            /// `Some` when this binding targets a foreign account (FPI); `None` for the
            /// transaction's active account.
            foreign_account_id: ::core::option::Option<::miden::AccountId>,
        }
    })
}

/// Returns the trait names (final path segments) the user already derives on the marker struct.
fn user_derived_names(attrs: &[Attribute]) -> HashSet<String> {
    attrs
        .iter()
        .filter(|attr| attr.path().is_ident("derive"))
        .filter_map(|attr| {
            attr.parse_args_with(
                syn::punctuated::Punctuated::<syn::Path, syn::Token![,]>::parse_terminated,
            )
            .ok()
        })
        .flatten()
        .filter_map(|path| path.segments.last().map(|segment| segment.ident.to_string()))
        .collect()
}

/// Creates the constructor (`new`) inherent impl for the account wrapper.
fn constructor_impl(account_struct: &ItemStruct) -> ItemImpl {
    let ident = &account_struct.ident;
    parse_quote! {
        impl #ident {
            /// Creates an account API wrapper bound to the given foreign account.
            ///
            /// Methods invoked on the returned value are dispatched through
            /// `execute_foreign_procedure` against `account_id`.
            #[inline(always)]
            pub fn new(account_id: ::miden::AccountId) -> Self {
                Self {
                    foreign_account_id: ::core::option::Option::Some(account_id),
                }
            }
        }
    }
}

/// Builds the SDK trait impls for the account wrapper struct.
///
/// Emits the `ActiveAccount` guard impl that rejects active-account ops on a foreign binding,
/// and the `AccountWrapper` marker impl through which the note/tx-script macros instantiate
/// the entrypoint account parameter.
fn active_account_impl(account_struct: &ItemStruct) -> TokenStream2 {
    let ident = &account_struct.ident;
    let message = format!(
        "active-account operation called on `{ident}` while it is bound to a foreign account; \
         active-account methods are only valid for the transaction's active account"
    );
    quote! {
        impl ::miden::active_account::ActiveAccount for #ident {
            #[inline(always)]
            fn __assert_active_account(&self) {
                if self.foreign_account_id.is_some() {
                    ::core::panic!(#message);
                }
            }
        }

        impl ::miden::active_account::AccountWrapper for #ident {}
    }
}

/// Returns the generated trait method name for a generated FPI free function.
fn method_ident(func: &ItemFn) -> syn::Result<syn::Ident> {
    let fn_name = func.sig.ident.to_string();
    let Some(method_name) = fn_name.strip_prefix(RUST_FUNCTION_PREFIX) else {
        return Err(Error::new(
            func.sig.ident.span(),
            format!(
                "expected generated FPI function name to start with `{}`",
                RUST_FUNCTION_PREFIX
            ),
        ));
    };

    // No dependency method name is reserved. Component methods live on the generated trait, so a
    // method that shares a name with the inherent constructor `new` or an `ActiveAccount` built-in
    // (e.g. `get_id`) coexists with it and is resolved by the caller with UFCS
    // (`<Wallet as Interface>::method(account, ..)`) rather than silently shadowing it.
    Ok(syn::Ident::new(method_name, func.sig.ident.span()))
}

/// Returns the original WIT function name represented by a generated FPI free function.
fn function_wit_name(func: &ItemFn) -> syn::Result<String> {
    Ok(method_ident(func)?.to_string().to_kebab_case())
}

/// Converts a procedure root into SDK `Word` construction tokens.
fn procedure_root_tokens(root: ProcedureRoot) -> TokenStream2 {
    let felts = root.felts.into_iter().map(|value| quote!(::miden::felt!(#value)));
    quote!(::miden::Word::new([#(#felts),*]))
}

/// Extracts exported procedure roots from a selected dependency's package.
///
/// The package was deserialized (and identity-checked) once during dependency resolution and is
/// reused here rather than re-read from disk.
fn load_dependency(
    dependency: SelectedDependency,
    trait_ident: syn::Ident,
) -> syn::Result<Dependency> {
    let import = dependency.import().to_owned();
    let module_path = import_module_path(&import);
    let package_path = dependency.package_path.clone();
    let package = dependency.package.clone();

    let mut roots = HashMap::new();
    for export in package.manifest.exports() {
        let PackageExport::Procedure(proc_export) = export else {
            continue;
        };
        let Some(root_key) = procedure_root_key_from_export_path(proc_export.path.as_ref()) else {
            continue;
        };

        if root_key.interface != import {
            continue;
        }
        roots.insert(root_key, procedure_root_from_digest(&proc_export.digest));
    }

    Ok(Dependency {
        module_path,
        trait_ident,
        package_path,
        import,
        roots,
    })
}

/// Maps dependency-defined WIT types to the normal bindings module generated by component/note.
pub(crate) fn dependency_type_with_entries(
    dependencies: &[SelectedDependency],
) -> Vec<(String, wit_bindgen_rust::WithOption)> {
    use heck::ToUpperCamelCase;

    dependencies
        .iter()
        .flat_map(|dependency| {
            let import = dependency.import();
            let module_path = import_module_path(import);
            dependency.type_names().iter().map(move |wit_type| {
                (
                    format!("{import}/{wit_type}"),
                    wit_bindgen_rust::WithOption::Path(format!(
                        "crate::bindings::{}::{}",
                        module_path,
                        wit_type.to_upper_camel_case()
                    )),
                )
            })
        })
        .collect()
}

/// Converts a fully-qualified WIT import path into the Rust module path generated by wit-bindgen.
pub(crate) fn import_module_path(import: &str) -> String {
    let without_version = import.split('@').next().unwrap_or(import);
    without_version
        .split([':', '/'])
        .filter(|segment| !segment.is_empty())
        .map(|segment| segment.to_snake_case())
        .collect::<Vec<_>>()
        .join("::")
}

/// Extracts the WIT interface/function key encoded in a package procedure export path.
fn procedure_root_key_from_export_path(path: &MasmPath) -> Option<ProcedureRootKey> {
    let interface = single_non_root_path_component(path.parent()?)?;
    let function = path.last()?;
    Some(ProcedureRootKey::new(interface, function))
}

/// Returns the only non-root path component if `path` has exactly one.
fn single_non_root_path_component(path: &MasmPath) -> Option<&str> {
    let mut component = None;

    for next in path.components() {
        let next = next.ok()?;
        match next {
            PathComponent::Root => continue,
            PathComponent::Normal(_) => {
                if component.replace(next.as_str()).is_some() {
                    return None;
                }
            }
        }
    }

    component
}

/// Converts a MAST digest word into literal field elements.
fn procedure_root_from_digest(digest: &miden_protocol::Word) -> ProcedureRoot {
    let elements = digest.as_elements();
    ProcedureRoot {
        felts: [
            elements[0].as_canonical_u64(),
            elements[1].as_canonical_u64(),
            elements[2].as_canonical_u64(),
            elements[3].as_canonical_u64(),
        ],
    }
}

