    #[test]
    fn build_wrapper_method_dispatches_through_account_binding() {
        let native_func: ItemFn = parse_quote! {
            pub fn get_count(key: ::miden::Word) -> ::miden::Felt {
            }
        };
        let foreign_func: ItemFn = parse_quote! {
            pub fn fpi_get_count(
                account_id_prefix: ::miden::Felt,
                account_id_suffix: ::miden::Felt,
                foreign_proc_root: ::miden::Word,
                key: ::miden::Word,
            ) -> ::miden::Felt {
            }
        };
        let native_module_path =
            vec![quote::format_ident!("counter"), quote::format_ident!("counter")];
        let foreign_module_path = vec![quote::format_ident!("miden_fpi_v1_x")];
        let root = ProcedureRoot {
            felts: [1, 2, 3, 4],
        };

        let method = build_wrapper_method(
            &native_func,
            &foreign_func,
            &native_module_path,
            quote!(__miden_foreign_account_wallet),
            &native_module_path,
            &foreign_module_path,
            root,
        )
        .expect("trait method must build");
        let rendered = quote!(#method).to_string();

        // It is a `&self` trait default method (no visibility, has a body).
        assert!(rendered.contains("fn get_count (& self"), "rendered: {rendered}");
        // Dispatch reads the binding through `AccountBinding`, not the private field.
        assert!(
            rendered.contains(
                ":: miden :: active_account :: AccountBinding :: foreign_account_id (self)"
            ),
            "rendered: {rendered}"
        );
        assert!(!rendered.contains("self . foreign_account_id"), "rendered: {rendered}");
        // Both the FPI and native call targets are present.
        assert!(rendered.contains("miden_fpi_v1_x :: fpi_get_count"), "rendered: {rendered}");
        assert!(rendered.contains("counter :: counter :: get_count"), "rendered: {rendered}");
    }

