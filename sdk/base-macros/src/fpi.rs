/// WIT function prefix reserved for generated foreign procedure imports.
pub(crate) const WIT_FUNCTION_PREFIX: &str = "fpi-";

/// Rust identifier prefix generated by wit-bindgen for FPI WIT functions.
pub(crate) const RUST_FUNCTION_PREFIX: &str = "fpi_";

/// Version of the private synthetic interface contract used for generated FPI imports.
const FPI_ABI_VERSION: &str = "v1";

/// Prefix for private synthetic FPI package names.
const FPI_PACKAGE_PREFIX: &str = "fpi";

/// Number of parameters prepended by the FPI calling convention.
const FPI_ABI_PARAM_COUNT: usize = 3;

/// Names of the parameters prepended by the FPI calling convention.
const FPI_ABI_PARAM_NAMES: [&str; FPI_ABI_PARAM_COUNT] =
    ["account-id-prefix", "account-id-suffix", "foreign-proc-root"];

/// Maps one real dependency interface to its private synthetic FPI interface.
#[derive(Clone, Debug, Eq, PartialEq)]
pub(crate) struct FpiImportSpec {
    /// Fully-qualified real dependency interface, including its version.
    source_import: String,
    /// Canonical package identity which owns the private synthetic interface.
    synthetic_package: PackageName,
    /// Interface name retained from the source dependency.
    synthetic_interface: String,
    /// Fully-qualified private synthetic interface identity.
    synthetic_import: String,
}

impl FpiImportSpec {
    /// Creates a deterministic FPI import specification for `source_import`.
    fn new(source_import: String) -> syn::Result<Self> {
        let (source_package, synthetic_interface) = parse_interface_id(&source_import)?;
        let synthetic_package = PackageName {
            namespace: source_package.namespace,
            name: format!("{FPI_PACKAGE_PREFIX}-{FPI_ABI_VERSION}-{}", source_package.name),
            version: source_package.version,
        };
        let synthetic_import = synthetic_package.interface_id(&synthetic_interface);
        Ok(Self {
            source_import,
            synthetic_package,
            synthetic_interface,
            synthetic_import,
        })
    }

    /// Returns the fully-qualified real dependency interface.
    pub(crate) fn source_import(&self) -> &str {
        &self.source_import
    }

    /// Returns the canonical private synthetic interface identity.
    pub(crate) fn synthetic_import(&self) -> &str {
        &self.synthetic_import
    }

    /// Returns the package which owns the private synthetic interface.
    #[cfg(any(test, feature = "internal-wit-emit"))]
    pub(crate) fn synthetic_package(&self) -> &PackageName {
        &self.synthetic_package
    }

    /// Returns the Rust module path generated for the private synthetic interface.
    pub(crate) fn synthetic_module_path(&self) -> String {
        import_module_path(&self.synthetic_import)
    }
}

/// Parses a fully-qualified WIT interface ID into its package and interface components.
fn parse_interface_id(import: &str) -> syn::Result<(PackageName, String)> {
    let invalid = || {
        Error::new(
            Span::call_site(),
            format!(
                "invalid FPI dependency interface `{import}`; expected \
                 `namespace:package/interface@version`"
            ),
        )
    };
    let (namespace, package_and_interface) = import.split_once(':').ok_or_else(invalid)?;
    let (package, interface_and_version) =
        package_and_interface.split_once('/').ok_or_else(invalid)?;
    let (interface, version) = interface_and_version.rsplit_once('@').ok_or_else(invalid)?;
    if namespace.is_empty() || package.is_empty() || interface.is_empty() || version.is_empty() {
        return Err(invalid());
    }
    let version = Version::parse(version).map_err(|_| invalid())?;

    Ok((
        PackageName {
            namespace: namespace.to_string(),
            name: package.to_string(),
            version: Some(version),
        },
        interface.to_string(),
    ))
}

/// Builds sorted, deduplicated FPI import specifications.
pub(crate) fn import_specs(imports: &[String]) -> syn::Result<Vec<FpiImportSpec>> {
    let mut imports = imports.to_vec();
    imports.sort_unstable();
    imports.dedup();
    imports.into_iter().map(FpiImportSpec::new).collect()
}

/// Derives a stable binding-world name from its canonical imports and FPI ABI version.
pub(crate) fn import_world_name(name: &str, imports: &[FpiImportSpec]) -> String {
    let mut identity = format!("{FPI_ABI_VERSION}\0");
    for import in imports {
        write!(identity, "{}:", import.source_import().len())
            .expect("writing to an in-memory string cannot fail");
        identity.push_str(import.source_import());
    }
    let digest = Blake3_256::hash(identity.as_bytes());
    let mut fingerprint = String::with_capacity(32);
    for byte in &digest.as_bytes()[..16] {
        write!(fingerprint, "{byte:02x}").expect("writing to an in-memory string cannot fail");
    }
    format!("{name}-{fingerprint}")
}

/// Renders the private import world used exclusively by `#[account]` bindings.
pub(crate) fn import_world_wit(name: &str, imports: &[FpiImportSpec]) -> String {
    // The prefix added after WIT resolution references these types directly. Importing the owner
    // up front keeps the world's dependency closure complete without mutating or re-elaborating it.
    let mut source_imports = vec![CORE_TYPES_INTERFACE.to_string()];
    for import in imports {
        if import.source_import() != CORE_TYPES_INTERFACE {
            source_imports.push(import.source_import().to_string());
        }
    }
    wit_world::import_world_wit(name, &source_imports)
}

/// Populates private synthetic interfaces with `fpi-` variants of real dependency functions.
pub(crate) fn inject_imports(
    resolve: &mut Resolve,
    world_id: WorldId,
    imports: &[FpiImportSpec],
) -> syn::Result<()> {
    if imports.is_empty() {
        return Ok(());
    }

    let core_types = resolve_core_types(resolve)?;
    let mut created_packages = HashSet::new();
    for import in imports {
        let source_id = find_source_interface(resolve, world_id, import.source_import())?;
        let (synthetic_id, created) =
            ensure_synthetic_interface(resolve, import, &mut created_packages)?;

        validate_reserved_fpi_namespace(
            import.source_import(),
            resolve.interfaces[source_id].functions.values(),
        )?;
        if created {
            inject_functions_into_synthetic_interface(
                resolve,
                source_id,
                synthetic_id,
                core_types,
            )?;
        }
        validate_synthetic_interface(resolve, source_id, synthetic_id, core_types)?;
        import_synthetic_interface(resolve, world_id, synthetic_id, import.synthetic_import())?;
    }

    #[cfg(debug_assertions)]
    resolve.assert_valid();

    Ok(())
}

/// Finds a real imported interface by its full canonical identity.
fn find_source_interface(
    resolve: &Resolve,
    world_id: WorldId,
    source_import: &str,
) -> syn::Result<InterfaceId> {
    resolve.worlds[world_id]
        .imports
        .values()
        .filter_map(|item| match item {
            WorldItem::Interface { id, .. } => Some(*id),
            _ => None,
        })
        .find(|id| interface_import_path(resolve, *id).as_deref() == Some(source_import))
        .ok_or_else(|| {
            Error::new(
                Span::call_site(),
                format!("failed to resolve FPI source interface `{source_import}` in import world"),
            )
        })
}

/// Finds or creates the canonical private interface for one FPI dependency.
///
/// The returned boolean is true when this call created the interface.
fn ensure_synthetic_interface(
    resolve: &mut Resolve,
    import: &FpiImportSpec,
    created_packages: &mut HashSet<PackageName>,
) -> syn::Result<(InterfaceId, bool)> {
    let package_id = match resolve.package_names.get(&import.synthetic_package) {
        Some(id) if created_packages.contains(&import.synthetic_package) => *id,
        Some(_) => {
            return Err(Error::new(
                Span::call_site(),
                format!(
                    "synthetic FPI package `{}` for dependency interface `{}` collides with a WIT \
                     package already loaded by the project",
                    import.synthetic_package,
                    import.source_import()
                ),
            ));
        }
        None => {
            let id = resolve.packages.alloc(WitPackage {
                name: import.synthetic_package.clone(),
                docs: Docs::default(),
                interfaces: Default::default(),
                worlds: Default::default(),
            });
            resolve.package_names.insert(import.synthetic_package.clone(), id);
            created_packages.insert(import.synthetic_package.clone());
            id
        }
    };

    if let Some(id) = resolve.packages[package_id]
        .interfaces
        .get(&import.synthetic_interface)
        .copied()
    {
        return Ok((id, false));
    }

    let id = resolve.interfaces.alloc(Interface {
        name: Some(import.synthetic_interface.clone()),
        types: Default::default(),
        functions: Default::default(),
        docs: Docs::default(),
        stability: Default::default(),
        package: Some(package_id),
        span: WitSpan::default(),
        clone_of: None,
    });
    resolve.packages[package_id]
        .interfaces
        .insert(import.synthetic_interface.clone(), id);
    Ok((id, true))
}

/// Imports one canonical synthetic interface into the generated binding world.
fn import_synthetic_interface(
    resolve: &mut Resolve,
    world_id: WorldId,
    synthetic_id: InterfaceId,
    synthetic_import: &str,
) -> syn::Result<()> {
    let previous = resolve.worlds[world_id].imports.insert(
        WorldKey::Interface(synthetic_id),
        WorldItem::Interface {
            id: synthetic_id,
            stability: Default::default(),
            span: WitSpan::default(),
        },
    );
    if previous.is_some() {
        return Err(Error::new(
            Span::call_site(),
            format!("duplicate private FPI interface `{synthetic_import}` in import world"),
        ));
    }
    Ok(())
}

/// Determines whether a generated free function represents an FPI WIT import.
pub(crate) fn is_fpi_import_function(func: &ItemFn) -> bool {
    matches!(func.vis, syn::Visibility::Public(_))
        && func.sig.unsafety.is_none()
        && func.sig.ident.to_string().starts_with(RUST_FUNCTION_PREFIX)
}

/// Determines whether a generated free function represents a plain (non-FPI) WIT import.
///
/// Unlike [`is_fpi_import_function`], which keys on the synthesized `fpi_` prefix, this is a
/// negative filter: it accepts any safe, public free function in a generated leaf import module
/// that is not an FPI import. That relies on wit-bindgen emitting exactly the interface's functions
/// as plain public free functions in those modules — it does not emit auxiliary public free
/// functions there. If a future wit-bindgen version changes that, such functions would be picked
/// up as sibling methods and this predicate would need to narrow (e.g. by the interface's declared
/// function set).
pub(crate) fn is_plain_import_function(func: &ItemFn) -> bool {
    matches!(func.vis, syn::Visibility::Public(_))
        && func.sig.unsafety.is_none()
        && !func.sig.ident.to_string().starts_with(RUST_FUNCTION_PREFIX)
}

