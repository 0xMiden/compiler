const I32_INTRINSICS_MASM: &str = include_str!("../../../../../codegen/masm/intrinsics/i32.masm");

/// Assembles an executable program that wraps `procedure_body` inside a procedure that is called
/// as entry point.
///
/// The intrinsics module `codegen/masm/intrinsics/i32.masm` is statically linked so the body can
/// call these intrinsics by their fully-qualified path.
fn assemble_test_program(procedure_body: &str) -> miden_processor::Program {
    let source_manager = Arc::new(DefaultSourceManager::default());

    // Parse the intrinsic module with its fully-qualified path
    let i32_intrinsics = I32_INTRINSICS_MASM
        .parse_with_options(
            source_manager.clone(),
            ParseOptions::new(ModuleKind::Library, "::intrinsics::i32"),
        )
        .expect("failed to parse i32 intrinsics module");

    // Parse the test module with its fully-qualified path
    let test_module_source = format!("pub proc test_i32_intrinsic\n{procedure_body}\nend");
    let test_module = test_module_source
        .parse_with_options(
            source_manager.clone(),
            ParseOptions::new(ModuleKind::Library, "::test"),
        )
        .expect("failed to parse test module");

    // Assemble the executable program, statically linking both modules
    let mut assembler = Assembler::new(source_manager);
    assembler
        .compile_and_statically_link(i32_intrinsics)
        .expect("failed to statically link i32 intrinsics");
    assembler
        .compile_and_statically_link(test_module)
        .expect("failed to statically link test module");
    assembler
        .assemble_program("begin\n    exec.::test::test_i32_intrinsic\nend\n")
        .expect("failed to assemble program")
}

/// Describes the trap expected by the execution of an intrinsic.
///
/// Variants mirror [`OperationError`] variants that can be produced by i32 intrinsics.
#[derive(Debug, Clone)]
enum TrapExpectation {
    /// Expect `FailedAssertion { err_code: 0, err_msg: None }`, produced by overflow traps.
    FailedAssertionOverflow,
    DivideByZero,
}

impl TrapExpectation {
    /// Returns `Ok(())` if `vm_err` matches the expectation.
    fn check(&self, vm_err: &ExecutionError) -> Result<(), String> {
        match (self, vm_err) {
            (
                TrapExpectation::FailedAssertionOverflow,
                ExecutionError::OperationError {
                    err:
                        OperationError::FailedAssertion {
                            err_code,
                            err_msg: None,
                        },
                    ..
                },
            ) if *err_code == Felt::new(0) => Ok(()),
            (
                TrapExpectation::DivideByZero,
                ExecutionError::OperationError {
                    err: OperationError::DivideByZero,
                    ..
                },
            ) => Ok(()),
            _ => Err(format!("expected err {:?} but VM produced: {:?}", self, vm_err)),
        }
    }
}

/// Proptest helper that assembles `proc_body` with the i32 intrinsics, executes the procedure with
/// inputs generated by `strategy`, and asserts that the outputs matches `to_expected`.
///
/// * `to_inputs`: converts a strategy value to the stack expected by `proc_body`, for instance
///   `(i32, i32) -> [Felt, Felt]`.
/// * `to_expected`: computes the expected output natively.
///   * Returns `Ok(Vec<i32>)` if program execution is expected to succeed. The vector contains
///     the values expected on the stack after intrinsic execution.
///   * Returns `Err(TrapExpectation)` if the Miden VM is expected to trap. The expectation is
///     compared against the actual VM error to ensure the trap reason matches.
fn test_i32_intrinsic<S, F1, F2>(proc_body: &str, strategy: S, to_inputs: F1, to_expected: F2)
where
    S: Strategy,
    S::Value: fmt::Debug,
    F1: Fn(&S::Value) -> Vec<Felt>,
    F2: Fn(&S::Value) -> Result<Vec<i32>, TrapExpectation>,
{
    let program = assemble_test_program(proc_body);

    let res = TestRunner::default().run(&strategy, |input_tuple| {
        let expected_result = to_expected(&input_tuple);
        let inputs = to_inputs(&input_tuple);
        let stack_inputs = StackInputs::new(&inputs).expect("invalid stack inputs");

        let vm_result = execute_sync(
            &program,
            stack_inputs,
            AdviceInputs::default(),
            &mut DefaultHost::default(),
            ExecutionOptions::default(),
        );

        match (expected_result, vm_result) {
            (Ok(expected), Ok(trace)) => {
                let output_felts = trace.stack_outputs().get_num_elements(expected.len());
                let outputs: Vec<i32> =
                    output_felts.iter().map(|f| f.as_canonical_u64() as u32 as i32).collect();
                prop_assert_eq!(outputs, expected);
                Ok(())
            }
            (Err(expectation), Err(vm_err)) => match expectation.check(&vm_err) {
                Ok(()) => Ok(()),
                Err(msg) => Err(proptest::test_runner::TestCaseError::fail(msg)),
            },
            (Ok(expected), Err(vm_err)) => Err(proptest::test_runner::TestCaseError::fail(
                format!("Expected success with output {:?} but VM trapped: {:?}", expected, vm_err),
            )),
            (Err(expectation), Ok(trace)) => {
                let outputs: Vec<i32> = trace
                    .stack_outputs()
                    .iter()
                    .map(|f| f.as_canonical_u64() as u32 as i32)
                    .collect::<Vec<_>>();
                Err(proptest::test_runner::TestCaseError::fail(format!(
                    "Expected VM trap ({:?}) but execution succeeded with outputs: {:?}",
                    expectation, outputs
                )))
            }
        }
    });

    match res {
        Err(TestError::Fail(reason, value)) => {
            panic!("Found minimal failing case: {value:?}\n{reason}")
        }
        Ok(_) => (),
        _ => panic!("Unexpected test result: {:?}", res),
    }
}

/// For a `i32` unary op the strategy produces `a`. The intrinsic expects stack `[a]`.
fn unary_i32op_input_to_stack(strategy_value: &i32) -> Vec<Felt> {
    vec![Felt::new(*strategy_value as u32 as u64)]
}

/// For a `i32` binary op the strategy produces `(a, b)`. The intrinsic expects stack `[b, a]`.
fn binary_i32op_inputs_to_stack(strategy_value: &(i32, i32)) -> Vec<Felt> {
    vec![
        Felt::new(strategy_value.1 as u32 as u64),
        Felt::new(strategy_value.0 as u32 as u64),
    ]
}

#[test]
fn i32_overflowing_add() {
    let proc_body = r#"
    # Stack: [b, a]
    exec.::intrinsics::i32::overflowing_add
    # Stack: [overflowed, result]
"#;
    test_i32_intrinsic(
        proc_body,
        NumericStrategy::<i32>::add_signed(),
        binary_i32op_inputs_to_stack,
        |(a, b): &(i32, i32)| {
            let (result, overflowed) = a.overflowing_add(*b);
            Ok(vec![overflowed as i32, result])
        },
    );
}

