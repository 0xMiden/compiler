use super::*;

#[test]
fn i64_extend32_s() {
    let span = SourceSpan::default();

    let (package, context) = compile_test_module([Type::I64], [Type::I64], |builder| {
        let block = builder.current_block();
        let input = block.borrow().arguments()[0] as ValueRef;
        let result = builder.sign_extend(input, Type::I32, Type::I64, span).unwrap();
        builder.ret(Some(result), span).unwrap();
    });

    // (input, expected_out)
    let cases: Vec<(u64, u64)> = Vec::from([
        (
            0b0000_0000_0000_0000_0000_0000_0000_0000_0000_0000_0000_0000_0000_0000_0000_0000,
            0b0000_0000_0000_0000_0000_0000_0000_0000_0000_0000_0000_0000_0000_0000_0000_0000,
        ),
        (
            0b0000_0000_0000_0000_0000_0000_0000_0000_0000_0000_0000_0000_0000_0000_0000_0001,
            0b0000_0000_0000_0000_0000_0000_0000_0000_0000_0000_0000_0000_0000_0000_0000_0001,
        ),
        (
            0b0000_0000_0000_0000_0000_0000_0000_0000_0111_1111_1111_1111_1111_1111_1111_1111,
            0b0000_0000_0000_0000_0000_0000_0000_0000_0111_1111_1111_1111_1111_1111_1111_1111,
        ),
        (
            0b0000_0000_0000_0000_0000_0000_0000_0000_1000_0000_0000_0000_0000_0000_0000_0000,
            0b1111_1111_1111_1111_1111_1111_1111_1111_1000_0000_0000_0000_0000_0000_0000_0000,
        ),
        (
            0b0000_0000_0000_0000_0000_0000_0000_0000_1111_1111_1111_1111_1111_1111_1111_1111,
            0b1111_1111_1111_1111_1111_1111_1111_1111_1111_1111_1111_1111_1111_1111_1111_1111,
        ),
        (
            0b0000_0000_0000_0000_0000_0000_0000_0000_1000_0000_0000_0000_0000_0000_0000_0001,
            0b1111_1111_1111_1111_1111_1111_1111_1111_1000_0000_0000_0000_0000_0000_0000_0001,
        ),
        (
            0b0000_0000_0000_0000_0000_0001_0000_0010_1000_0000_0000_0000_0000_0000_0000_0000,
            0b1111_1111_1111_1111_1111_1111_1111_1111_1000_0000_0000_0000_0000_0000_0000_0000,
        ),
        (
            0b0000_1010_0000_1011_0000_1100_0000_1101_0111_1111_1111_1111_1111_1111_1111_1111,
            0b0000_0000_0000_0000_0000_0000_0000_0000_0111_1111_1111_1111_1111_1111_1111_1111,
        ),
        (
            0b1111_1111_1111_1111_1111_1111_1111_1111_1111_1111_1111_1111_1111_1111_1111_1111,
            0b1111_1111_1111_1111_1111_1111_1111_1111_1111_1111_1111_1111_1111_1111_1111_1111,
        ),
        (
            0b1111_1111_1111_1111_1111_1111_1111_1111_0000_0000_0000_0000_0000_0000_0000_0000,
            0b0000_0000_0000_0000_0000_0000_0000_0000_0000_0000_0000_0000_0000_0000_0000_0000,
        ),
    ]);

    for (input, expected_out) in cases {
        assert_eq!(((input as i32) as i64) as u64, expected_out, "invalid test case");

        let mut args = Vec::new();
        input.push_to_operand_stack(&mut args);

        eval_package::<u64, _, _>(&package, None, &args, context.session(), |trace| {
            let out = trace.parse_result::<u64>().unwrap();
            assert_eq!(out, expected_out);
            Ok(())
        })
        .unwrap();
    }
}
